int main() // 메인함수
{
	double r = 0, theta = 0; // 실수형 변수 r, theta 선언

	printf("직각삼각형의 빗변길이와 각도 입력 :"); // 콘솔창에 큰 따옴표 속 말을 띄우기 위해 printf 사용
	scanf("%lf %lf", &r, &theta); // 입력된 수를 차례대로 변수에 저장

	double pi = 3.141592; // 실수형 변수 pi 선언
	double rad = theta * pi / 180.0; // 실수형 변수 rad 선언 (수식은 변수 안에 저장)
	double x = r * cos(rad); // 실수형 변수 x 선언 (수식은 변수에 저장)
	double y = r * sin(rad); // 실수형 변수 y 선언 (수식은 변수에 저장)

	printf("직각삼각형의 밑변 %lf 높이 %lf 입니다.", x, y); // 큰 따옴표 속 두 개의 %lf에 변수를 차례대로 참조 후 콘솔창에 띄움

	return 0; // 메인함수의 종료를 알림

}
