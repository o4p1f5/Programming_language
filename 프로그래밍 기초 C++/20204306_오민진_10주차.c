//2 // 난수를 뽑아내는 연습
int main() // 메인함수 선언
{
	srand((unsigned)time(NULL)); 
	// srand() : 난수를 생성하는 seed를 변경하기위한 선언
	// unsigned : 정수자료형앞에만 붙음, 표현가능한 양의 정수의 2배 표현가능
	// time() : 현재시간을 계산해서 반환해주는 함수 
	// NULL : 숫자 0을 의미하는 매크로 상수값
	for (int i = 1; i <= 100; i++) // 정수형 변수 i의 값을 1로 저장, 변수 i의 값이 100보다 커지기전까지 i에 1을 더해가며 반복
	{
		printf("%10d", rand()); // printf함수를 이용해 %10d에 0 ~ 32767 의 숫자를 생성하는 함수에서 무작위로 뽑아낸 숫자 대입 후 출력 
								// rand()에 선언된 RAND_MAX : 0x7fff(16진수) = 32767(10진수)
		if (i % 10 == 0) // 변수 i를 10으로 나눈 나머지의 값이 0과 같은 지 판단
		{
			printf("\n"); // 맞다면 줄 바꿈 = 숫자 10개 나온 후 줄 바꿈
		}
	}
} 

