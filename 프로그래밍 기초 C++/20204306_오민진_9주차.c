//5 // 입력받은 세 수로 2차방정식의 해를 구하는 코드 
int main() // 메인함수 선언 
{
	while (1) // break를 만날때까지 무한루프
	{
		double a = 0, b = 0, c = 0; // 실수형 변수 a,b,c 선언

		printf("ax^2 + bx + c = 0의 계수 입력 :"); // 콘솔창에 출력
		scanf("%lf %lf %lf", &a, &b, &c); // 입력받은 세 수를 차례대로 a,b,c에 저장

		if (a == 0 || b == 0 || c == 0) // 만약 a,b,c가 0이라면
		{
			printf("프로그램 종료"); // 콘솔창에 '프로그램 종료' 출력
			break; // 반복문 종료
		} 
		else // a,b,c가 0이 아니라면
		{
			double ident = (b * b - 4.0 * a * c); // 실수형 변수 ident에 오른쪽 a,b,c를 이용한 식의 계산값을 저장

			if (ident > 0) // 변수 ident가 0보다 크다면
			{
				double x1 = (-b + sqrt(ident)) / (2.0 * a); // 실수형 변수 x1에 a,b,c를 이용한 식의 계산값을 저장
				double x2 = (-b - sqrt(ident)) / (2.0 * a); // 실수형 변수 x2에 a, b, c를 이용한 식의 계산값을 저장

				printf("두 개의 실근 : x1 = %.2lf x2 = %.2lf\n", x1, x2); // printf함수를 이용해 %.2lf에 변수를 차례로 대입 후 출력

			}
			else if (ident == 0) // 변수 ident의 값이 0이라면
			{
				double x1 = 0, x2 = 0; // 실수형 변수 x1,x2 선언
				x1 = x2 = -b / (2.0 * a); // x1과 x2의 값은 같고 오른쪽 식의 계산값을 두 변수에 저장

				printf("중근 x1 = %.2lf x2 = %.2lf\n", x1, x2); // printf함수를 이용해 %.2lf에 변수를 차례로 대입 후 출력

			}
			else if (ident < 0) // 변수 ident의 값이 0보다 작다면
			{
				double r = -b / (2.0 * a); // 실수형 변수 r에 오른쪽 식의 계산값을 저장
				double i = sqrt(-ident) / (2.0 * a); // 실수형 변수 i에 오른쪽 식의 계산값을 저장

				if (i < 0) // i가 음수인지 판단
				{
					printf("두 개의 허근 : x1 = %.2lf + j %.2lf x2 = %.2lf - j %.2lf\n", r, i * -1, r, i*-1); // printf함수를 이용해 %.2lf에 변수를 차례로 대입 후 출력 // i가 음수면 -1을 곱해줘 양수로 만듦
				}
				else // i가 양수라면
				{
					printf("두 개의 허근 : x1 = %.2lf + j %.2lf x2 = %.2lf - j %.2lf\n", r, i, r, i); // printf함수를 이용해 %.2lf에 변수를 차례로 대입 후 출력
				}
			}
			
		}
	}
	return 0;
}